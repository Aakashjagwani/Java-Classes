package valueObject;

public class Vob {
		private int id;
		private String fn;
		private String ln;
		
		public void setId(int id) {
			this.id = id;
		}
		public int getId() {
			return id;
		}
		public String getFn() {
			return fn;
		}
		public void setFn(String fn) {
			this.fn = fn;
		}
		public String getLn() {
			return ln;
		}
		public void setLn(String ln) {
			this.ln = ln;
		}
			
}
